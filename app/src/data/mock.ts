export const getNewsById = (id: string): NewsArticle | undefined =>
  newsArticles.find(n => n.id === id);

export const getNewsBySlug = (slug: string): NewsArticle | undefined =>
  newsArticles.find(n => n.slug === slug);

export const getNewsByCategory = (slug: string): NewsArticle[] =>
  newsArticles.filter(n => n.category.slug === slug);

export const getCommentsByNewsId = (newsId: string): Comment[] =>
  comments.filter(c => c.newsId === newsId);

export const getRelatedNews = (article: NewsArticle, count = 3): NewsArticle[] => {
  return newsArticles
    .filter(n => n.id !== article.id && (n.category.id === article.category.id || n.tags.some(t => article.tags.includes(t))))
    .slice(0, count);
};

